#include "libft.h"

void	*ft_memmove(void *dst, const void *src, size_t n)
{
	size_t	i;

	if (dst > src)
	{
		i = 1;
		while (i <= n)
		{
			((unsigned char *)dst)[n - i] = ((unsigned char *)src)[n - i];
			i++;
		}
	}
	else if (dst < src)
	{
		i = 0;
		while (i < n)
		{
			((unsigned char *)dst)[i] = ((unsigned char *)src)[i];
			i++;
		}
	}
	return (dst);
}
